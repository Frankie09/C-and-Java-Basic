
public class ForJava {
    
    
    public static void main(String[] args) {
        
        for (int i = 1; i < 6; i++) {
            for (int h = 0; h < i; h++) {
                System.out.print("*");
            }
            
            for (int x = 5; x > i; x--) {
            System.out.print(" ");
        }
            System.out.println("");
        
        }
    }
    
}
